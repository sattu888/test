#count()
l=[10,20,30,50,10,10]
c=l.count(10)
print(c)

#maximum
l=[10,20,30,50,10,10]
m=max(l)
print(m)

#minimum
l=[10,20,30,50,10,10]
m=min(l)
print(m)

#sort
l=[10,20,30,50,10,10]
l=sorted(l)
print(l)

#reverse
l=[10,20,30,50,10,10]
l.reverse(l)
print(l)

#index
l=[10,20,30,50,10,10]
l.index(20)
print(l)